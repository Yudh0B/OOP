class modifier {
    /*
    modifier ada 2 kelompok
    - access modifier : mengatur level akses
    - non access modifier : tidak mengatur level akses, tapi menyediakan fungsionalitas yang lain
    
    ACCESS MODIFIER
    untuk class access modifier yang bisa digunakan : public atau default
    public : class dapat diakses oleh beberapa class yang lain
    default : class hanya bsia diakses oleh class2 yang ada dalam 1 package (folder)
    
    untuk method, constructor, attribute access modifier yang digunakan : public, private, protected, default
    public : kode bisa diakses dari semua class
    private : kode hanya bisa diakses dalam class yang dideklarasikan
    protected : kode dapat diakses dalam paket yang sama dan subclass (turunan)
    default : kode hanya bisa diakses dalam package yang sama
    
    NON ACCESS MODIFIER
    untuk class, non access modifier yang bisa digunakan : final atau astract
    final : class tidak bisa diturunkan oleh class yang lain
    abstract : class yang tidak bisa digunakan untuk membuat objek, harus diturunkan ke kelas lain terlebih dulu

    untuk attribute dan method : final, static, abstract, transient, synchronized, volatile
    final : method atau atribut tidak dimodifikasi atau di overide
    static : atribut dan method dimiliki oleh class tidak dalam objek
    abstract : hanya bisa digunakan dalam class abstract
    transient : atribut atau method dilompati ketika serializing objek yang memuatnya
    synchronized : method hanya dapat diakses oleh 1 thread dalam 1 waktu
    
    J2EE : JAVA 2ND Tech Enterprise Edition
    J2ME : Java 2nd TEch Micro Edition
    J2SE : Standard Edition (JSP Servlet)

    */


}
